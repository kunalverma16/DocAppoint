	<h2>Why Antioxidants Are Good for You</h2>
	<h5>Dr. Prabha AcharyaCosmetologist</h5>
	<h6>18 February, 2018</h6><br><br>

<p>
	
<p>Antioxidants neutralize harmful free radicals in your body, which can cause several serious or life-threatening diseases and health problems including heart disease, cancer, immune dysfunction, diabetes, etc.</p>

<p>Free radicals in your blood vessels -usually caused by things like deep-fried foods, cigarette smoke, or air pollution - change the structure of the (bad) LDL cholesterol so it becomes more liable to gum up your arteries and cause heart problems. Harmful free radicals exist everywhere in our environment, but they are concentrated in pollution, chemicals, pesticides, alcohol, drugs, unhealthy food, etc.</p>

<p>Why are antioxidants good for you?</p>

<p>Reason: Antioxidants help to keep your cells healthy and youthful, which can prevent age-related illnesses, relieve stress and increase your lifespan. </p>

<p>Benefits of eating antioxidant-rich food are:</p>

<ul><li>Slower signs of ageing by Reducing Free Radical Damage.</li></ul> 
<ul><li>Healthier, more youthful, glowing skin. </li></ul>
<ul><li>Reduced cancer risk.</li> </ul>
<ul><li>Longer Lifespan. </li></ul>
<ul><li>Protection against heart disease and stroke.</li> </ul>
<ul><li>Reduced risk for vision loss or disorders like macular degeneration and cataracts, etc.</li> </ul>
<ul><li>Sources: Most common fruits, vegetables and herbs in the diet that contain antioxidants include forms like vitamin E, selenium, vitamin C, beta-carotene, flavonoids and lycopene (for example Cruciferous vegetables, citrus fruits, red wine, pomegranate, berries, tomatoes etc).</li></ul>


